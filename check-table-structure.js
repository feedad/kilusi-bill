const { query } = require('./config/database');

async function checkTableStructure() {
    try {
        console.log('üîç Checking customers table structure...');

        // Get table columns
        const result = await query(`
            SELECT column_name, data_type, is_nullable, column_default
            FROM information_schema.columns
            WHERE table_name = 'customers'
            ORDER BY ordinal_position
        `);

        console.log('\nüìä Customers Table Structure:');
        console.log('==============================');

        if (result.rows.length === 0) {
            console.log('‚ùå No columns found in customers table');
        } else {
            result.rows.forEach(column => {
                console.log(`üìù ${column.column_name}`);
                console.log(`   Type: ${column.data_type}`);
                console.log(`   Nullable: ${column.is_nullable}`);
                console.log(`   Default: ${column.column_default || 'NULL'}`);
                console.log('---');
            });
        }

        // Get sample data
        console.log('\nüìä Sample Customer Data:');
        console.log('==========================');

        const sampleData = await query(`
            SELECT *
            FROM customers
            LIMIT 3
        `);

        if (sampleData.rows.length === 0) {
            console.log('‚ùå No data found in customers table');
        } else {
            sampleData.rows.forEach((customer, index) => {
                console.log(`üë§ Customer ${index + 1}:`);
                Object.keys(customer).forEach(key => {
                    console.log(`   ${key}: ${customer[key]}`);
                });
                console.log('---');
            });
        }

    } catch (error) {
        console.error('‚ùå Error checking table structure:', error.message);
    } finally {
        process.exit(0);
    }
}

checkTableStructure();